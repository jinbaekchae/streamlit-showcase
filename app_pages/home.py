"""홈 · 개요 페이지."""

import streamlit as st

from utils.ui import footer

st.title("🎈 Streamlit 쇼케이스")
st.subheader("파이썬 스크립트 하나로 만드는 데이터 웹앱, 10가지 예시")

st.markdown(
    """
Streamlit(스트림릿)은 **HTML·CSS·자바스크립트 없이 파이썬 코드만으로 웹앱을 만드는 도구**입니다.
`st.button()` 같은 함수를 부르면 화면에 버튼이 생기고, 값이 바뀌면 스크립트가 위에서 아래로
다시 실행되며 화면이 자동으로 갱신됩니다. 서버·라우팅·상태관리를 직접 다루지 않아도 됩니다.

아래 10개 데모는 각각 Streamlit의 서로 다른 강점을 하나씩 보여주도록 구성했습니다.
왼쪽 사이드바에서 순서대로 확인하실 수 있습니다.
"""
)

col1, col2, col3, col4 = st.columns(4)
col1.metric("데모 개수", "10개")
col2.metric("사용 언어", "파이썬만")
col3.metric("직접 짠 HTML", "0줄")
col4.metric("배포 비용", "무료")

st.divider()

st.markdown("### 데모 10선")

demos = [
    ("📊", "① 매출 대시보드", "필터·KPI·차트·다운로드를 갖춘 실무형 대시보드. 사이드바 조작이 즉시 반영."),
    ("🔎", "② 데이터 탐색기", "표를 화면에서 직접 편집·정렬·검색. 엑셀 같은 상호작용을 코드 몇 줄로."),
    ("💬", "③ 챗봇", "채팅 UI 기본 제공. 대화 기록 유지와 글자가 흘러나오는 스트리밍까지."),
    ("🤖", "④ 예측 시뮬레이터", "슬라이더를 움직이면 결과가 실시간 재계산. 반응형 UI의 핵심을 보여줌."),
    ("💱", "⑤ 환율 대시보드", "외부 API 실시간 조회 + 캐시. 무료 환율 API로 원/달러 추이 표시."),
    ("🖼️", "⑥ 이미지 편집기", "이미지 업로드·필터 적용·결과 다운로드. 미디어 처리도 파이썬으로."),
    ("📝", "⑦ 설문 폼", "여러 입력을 묶어 한 번에 제출하는 폼. 응답 누적·집계·내려받기."),
    ("🗺️", "⑧ 지도 시각화", "위경도만 있으면 지도가 한 줄로. 지점 분포를 지도 위에 표시."),
    ("✍️", "⑨ 텍스트 분석기", "글자·단어·문장 수와 빈출 단어를 실시간 분석. 입력하는 즉시 갱신."),
    ("🧮", "⑩ ROI 계산기", "투자 회수 시점·손익분기점을 계산. 비즈니스 계산기를 웹앱으로."),
]

for i in range(0, len(demos), 2):
    left, right = st.columns(2)
    for col, (icon, name, desc) in zip((left, right), demos[i : i + 2]):
        with col:
            with st.container(border=True):
                st.markdown(f"#### {icon} {name}")
                st.write(desc)

st.divider()

st.info(
    "각 데모 페이지 맨 아래의 **'🧩 이 데모가 보여주는 스트림릿 기능'** 을 펼치면, "
    "그 화면이 어떤 기능으로 만들어졌는지 정리해 두었습니다.",
    icon="💡",
)

footer()
